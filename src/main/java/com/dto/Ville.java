package com.dto;

public class Ville {
	
	private String codeCommune;
	private String nomCommune;
	private String codePostal;
	private String libelleAacheminement;
	private String ligne_5;
	private String latitude;
	private String longitude;
	
	public String getCodeCommune() {
		return codeCommune;
	}
	public void setCodeCommune(String codeCommune) {
		this.codeCommune = codeCommune;
	}
	public String getNomCommune() {
		return nomCommune;
	}
	public void setNomCommune(String nomCommune) {
		this.nomCommune = nomCommune;
	}
	public String getCodePostal() {
		return codePostal;
	}
	public void setCodePostal(String codePostal) {
		this.codePostal = codePostal;
	}
	public String getLibellAacheminement() {
		return libelleAacheminement;
	}
	public void setLibellAacheminement(String libellAacheminement) {
		this.libelleAacheminement = libellAacheminement;
	}
	public String getLigne_5() {
		return ligne_5;
	}
	public void setLigne_5(String ligne_5) {
		this.ligne_5 = ligne_5;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	
}
